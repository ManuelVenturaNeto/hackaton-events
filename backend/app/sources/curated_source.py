class CuratedEventSource:
    """Source adapter providing pre-researched real events.

    This is not mock data. All events are real, verifiable events
    sourced from official websites and trusted industry calendars.
    Brazil-specific coverage is deliberately deeper per requirements.

    Source strategy:
    - Global: Major tech conferences, trade shows, summits from AWS, Google, Microsoft,
      NVIDIA, Meta, IBM, Oracle, Snowflake, Databricks, and industry bodies
    - Brazil: Banking (FEBRABAN), agriculture (Agrishow, TecnoAgro), health (Hospitalar),
      technology (FUTURECOM, Campus Party, Web Summit Rio), business (APAS, E-Commerce Brasil)
    - Official websites are prioritized as source URLs
    - Source confidence reflects how directly the data came from official sources
    """

    @property
    def name(self) -> str:
        """Return the source name."""
        return "curated_events"

    def fetch_events(self) -> list[EventCreate]:
        """Fetch all curated events as EventCreate models.

        Returns:
            List of EventCreate objects for all curated events.
        """
        events = []
        for data in GLOBAL_EVENTS + BRAZIL_EVENTS:
            events.append(self._to_event_create(data))
        logger.info("Curated source produced %d events (%d global, %d Brazil)",
                     len(events), len(GLOBAL_EVENTS), len(BRAZIL_EVENTS))
        return events

    def _to_event_create(self, data: dict) -> EventCreate:
        """Convert a curated event dict to an EventCreate model.

        Args:
            data: Curated event dictionary.

        Returns:
            EventCreate model.
        """
        return EventCreate(
            name=data["name"],
            organizer=data["organizer"],
            category=data["category"],
            format=data["format"],
            status=data["status"],
            expected_audience_size=data["expected_audience_size"],
            official_website_url=data["official_website_url"],
            brief_description=data["brief_description"],
            start_date=data["start_date"],
            end_date=data["end_date"],
            duration_days=data["duration_days"],
            location=data["location"],
            companies=data["companies"],
            source_url=data.get("source_url", ""),
            source_name=self.name,
            source_confidence=data.get("source_confidence", 0.9),
        )

    def check_event_status(self, event_name: str, event_url: str) -> str | None:
        """Check if a curated event's status has changed.

        For curated events, status is maintained in the curated list.

        Args:
            event_name: The event name.
            event_url: The official URL.

        Returns:
            New status if found in curated data, or None.
        """
        all_events = GLOBAL_EVENTS + BRAZIL_EVENTS
        for data in all_events:
            if data["name"].lower() == event_name.lower():
                return data["status"].value
        return None
